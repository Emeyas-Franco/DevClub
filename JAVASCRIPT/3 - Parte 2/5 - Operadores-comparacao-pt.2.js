/*

OPERADORES DE COMPARAÇÃO

> -> Maior
< -> Menor

>= -> Maior ou igual
<= -> Menor ou igual

*/

const firstnumber = 20
const seccondnumber = 30

console.log(firstnumber > seccondnumber)
console.log(firstnumber < seccondnumber)
console.log(firstnumber >= seccondnumber)
console.log(firstnumber <= seccondnumber)
