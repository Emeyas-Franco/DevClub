/*

Operadores de atribuição

 = -> Atribuição
+= -> Adição
-= -> Subtração
/= -> Divisão
*= -> Multiplicação
%= -> Resto

*/

let result = 20

result += 70 // result = result + 70
console.log(result)
result -= 50 // result = result - 50
console.log(result)
result /= 70 // result = result / 70
console.log(result)
result *= 50 // result = result * 50
console.log(result)
result %= 70 // result = result % 70
 
console.log(result)

